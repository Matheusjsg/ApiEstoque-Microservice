package Estoque.InfraStructure.entities;

import jakarta.persistence.*;


@Entity
@Table(name = "tb_imagens")
public class ImagemEntity {


        @Id
        @GeneratedValue(strategy = GenerationType.IDENTITY)
        private long id;
        private String nomeArquivo;
        @ManyToOne
        @JoinColumn(name = "produto_id")
        private ProductsEntity produto;


public ImagemEntity() {}


    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNomeArquivo() {
        return nomeArquivo;
    }

    public void setNomeArquivo(String nomeArquivo) {
        this.nomeArquivo = nomeArquivo;
    }

    public ProductsEntity getProduto() {
        return produto;
    }

    public void setProduto(ProductsEntity produto) {
        this.produto = produto;
    }

    public ImagemEntity(String nomeArquivo, ProductsEntity produto) {
            this.nomeArquivo = nomeArquivo;
            this.produto = produto;
        }


}
